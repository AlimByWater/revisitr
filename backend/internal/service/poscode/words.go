// Normalize canonicalizes a Russian word for matching:
//   - lowercase
//   - trim surrounding whitespace and collapse all internal whitespace away
//   - fold ё -> е and й -> и (both cases)
//
// It operates on []rune so multi-byte Cyrillic characters are handled
// correctly; byte-level operations would corrupt them.
func Normalize(s string) string {
	s = strings.ToLower(strings.TrimSpace(s))

	out := make([]rune, 0, len(s))
	for _, r := range s {
		switch r {
		case ' ', '\t', '\n', '\r', '\f', '\v':
			// drop all internal whitespace
			continue
		case 'ё', 'Ё':
			out = append(out, 'е')
		case 'й', 'Й':
			out = append(out, 'и')
		default:
			out = append(out, r)
		}
	}
	return string(out)
}

// runeLen returns the number of runes (not bytes) in s.
func runeLen(s string) int {
	n := 0
	for range s {
		n++
	}
	return n
}

